package Objects;

public class GroundBlock 
{
  int x;
  int y;
  private GroundBlock groundBlock;
  
  public GroundBlock(int x, int y) 
  {
    this.x = x;
    this.y = y;
  }
  
  public void setGroundBlock(int x, int y) 
  {
    this. x = x;
    this. y = y;
  }
  
  public GroundBlock getGroundBlock() 
  {
    return this.groundBlock;
  }

}
